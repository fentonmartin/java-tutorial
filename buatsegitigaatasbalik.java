class buatsegitigaatasbalik {
public static void main(String[]args) {
  System.out.println("\n\tHello, welcome to \"fencode.blogspot.com\".");
  System.out.println("\t       visit : \"bit.ly/fencode\"\n\n");

int e = 0;
for(int a = 0; a < 10; a++) {
  for(int b = 0; b < e; b++) {
    System.out.print("  ");
  }
  for(int c = 10; c > e; c--) {
    System.out.print(" *");
  }
  System.out.println("");
  e = e + 1;
}
}
}
